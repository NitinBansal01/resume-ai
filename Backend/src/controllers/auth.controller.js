const userModel = require("../models/user.model")
const bcrypt = require("bcryptjs")
const jwt = require("jsonwebtoken")
const tokenBlacklistModel = require("../models/blacklist.model")

/**
 * @name registerUserController
 * @description register a new user, expects username, email and password in the request body
 * @access Public
 */
// the flow now comes to registerUserController function which is defined in auth.controller.js file. This function is responsible for handling the user registration process. It takes in the request and response objects as parameters and performs the following steps:
// 1. Extracts the username, email, and password from the request body.
// 2. Checks if any of the required fields are missing and returns a 400 status code with an error message if they are. 
// 3. Checks if a user with the same username or email already exists in the database and returns a 400 status code with an error message if they do.
// 4. Hashes the password using bcryptjs and creates a new user in the database with the provided username, email, and hashed password.
// 5. Generates a JWT token for the newly registered user and sets it as a cookie in the response.
// 6. Returns a 201 status code with a success message and the user details in the response body.
async function registerUserController(req, res) {

    const { username, email, password } = req.body

    if (!username || !email || !password) {
        return res.status(400).json({
            message: "Please provide username, email and password"
        })
    }

    const isUserAlreadyExists = await userModel.findOne({
        $or: [ { username }, { email } ]
    })

    if (isUserAlreadyExists) {
        return res.status(400).json({
            message: "Account already exists with this email address or username"
        })
    }

    const hash = await bcrypt.hash(password, 10)

    const user = await userModel.create({
        username,
        email,
        password: hash
    })

    const token = jwt.sign(
        { id: user._id, username: user.username },
        process.env.JWT_SECRET,
        { expiresIn: "1d" }
    )//the _id is a unique identifier automatically generated by MongoDB user in a database. It is used to uniquely identify each user in the database and can be used to retrieve user information or perform operations on a specific user. 

    //do we need to create token during registration? Yes, we need to create a token during registration because it allows the user to be automatically logged in after they have successfully registered. By generating a JWT token and sending it back to the client, we can authenticate the user and provide them with access to protected routes without requiring them to log in again. This improves the user experience by streamlining the registration and login process.

    res.cookie("token", token, {
    httpOnly: true,
    secure: true,
    sameSite: "none",
    maxAge: 24 * 60 * 60 * 1000
})//this token goes to the cookie of the user and it will be used for authentication in future requests. The token is stored in a cookie called "token" and is sent back to the client in the response. The client can then include this cookie in subsequent requests to authenticate the user and access protected routes.


    res.status(201).json({
        message: "User registered successfully",
        user: {
            id: user._id,
            username: user.username,
            email: user.email
        }
    })

}


/**
 * @name loginUserController
 * @description login a user, expects email and password in the request body
 * @access Public
 */
async function loginUserController(req, res) {

    const { email, password } = req.body

    const user = await userModel.findOne({ email })

    if (!user) {
        return res.status(400).json({
            message: "Invalid email or password"
        })
    }

    const isPasswordValid = await bcrypt.compare(password, user.password)
    //here user.password is the hashed password stored in the database and password is the plain text password provided by the user during login. The bcrypt.compare() function takes these two values and compares them to check if they match. It returns a boolean value indicating whether the passwords are valid or not.

    if (!isPasswordValid) {
        return res.status(400).json({
            message: "Invalid email or password"
        })
    }

    const token = jwt.sign(
        { id: user._id, username: user.username },
        process.env.JWT_SECRET,
        { expiresIn: "1d" }
    )//the format of sign method is jwt.sign(payload, secretOrPrivateKey, [options, callback]). The payload is the data that we want to include in the token, such as the user's id and username. The secretOrPrivateKey is a string that is used to sign the token and should be kept secret. The options parameter can include additional settings for the token, such as the expiration time. In this case, we are setting the token to expire in 1 day.


    res.cookie("token", token, {
    httpOnly: true,
    secure: true,
    sameSite: "none",
    maxAge: 24 * 60 * 60 * 1000
})
    // the cookie goes into frontend and it will be used for authentication in future requests. The token is stored in a cookie called "token" and is sent back to the client in the response. cookie is stored in the browser and it will be sent with every subsequent request to the server, allowing the server to authenticate the user and provide access to protected routes.

    res.status(200).json({
        message: "User loggedIn successfully.",
        user: {
            id: user._id,
            username: user.username,
            email: user.email
        }
    })
}


/**
 * @name logoutUserController
 * @description clear token from user cookie and add the token in blacklist
 * @access public
 */
async function logoutUserController(req, res) {
    const token = req.cookies.token

    if (token) {
        await tokenBlacklistModel.create({ token })
    }//needs to be blacklisted because if the user logs out and the token is not blacklisted, the user can still use the same token to access protected routes. By adding the token to a blacklist, we can ensure that it is no longer valid and cannot be used for authentication.

    res.clearCookie("token", {
    httpOnly: true,
    secure: true,
    sameSite: "none"
})

    res.status(200).json({
        message: "User logged out successfully"
    })
}

/**
 * @name getMeController
 * @description get the current logged in user details.
 * @access private
 */
async function getMeController(req, res) {

    const user = await userModel.findById(req.user.id)
    // some common functions of model are findById, findOne, find, create, updateOne, deleteOne, etc. These functions are used to interact with the database and perform CRUD operations on the data stored in the database. The findById function is used to retrieve a single document from the database based on its unique identifier (_id). It takes the _id as an argument and returns the corresponding document if it exists in the database.
    //the auth middleware already attaches the user information to the req object (e.g., req.user = decoded) after verifying the token. then why do we need to fetch the user details from the database again? We need to fetch the user details from the database again because the information stored in the JWT token may not be up-to-date or complete. The token only contains a limited set of user information (e.g., id and username) that was encoded during registration or login. By fetching the user details from the database, we can ensure that we have the most current and complete information about the user, such as their email address or any other relevant data that may have changed since the token was issued.



    res.status(200).json({
        message: "User details fetched successfully",
        user: {
            id: user._id,
            username: user.username,
            email: user.email
        }
    })//res sends the response back to the client with a status code of 200 (OK) and a JSON object containing a message and the user details. The user details include the user's id, username, and email, which are retrieved from the database using the findById function. This allows the client to access the current logged-in user's information and display it in the frontend application.

}



module.exports = {
    registerUserController,
    loginUserController,
    logoutUserController,
    getMeController
}